    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplication.LaunchOptionsKey: Any]?) -> Bool {
        // Configure audio session to play even when device is in silent mode
        let audioSession = AVAudioSession.sharedInstance()
        do {
            // IMPORTANT: .playback category (without .mixWithOthers) is required for silent mode
            // Using .mixWithOthers can prevent audio from playing in silent mode on some iOS versions
            try audioSession.setCategory(.playback, mode: .default, options: [])
            try audioSession.setActive(true, options: [])
            print("[Audio] AVAudioSession configured: category=\(audioSession.category), mode=\(audioSession.mode)")
        } catch {
            print("[Audio] Failed to configure AVAudioSession: \(error)")
        }

        return true
    }

